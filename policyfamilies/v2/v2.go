func (c *Client) ListPolicyFamiliesIter(ctx context.Context, req *ListPolicyFamilies, opts ...api.Option) iter.Seq2[*PolicyFamily, error] {
	return func(yield func(*PolicyFamily, error) bool) {
		// Deep copy the request via JSON round-trip to avoid modifying the original.
		reqBody, err := json.Marshal(req)
		if err != nil {
			yield(nil, err)
			return
		}
		pageReq := ListPolicyFamilies{}
		if err := json.Unmarshal(reqBody, &pageReq); err != nil {
			yield(nil, err)
			return
		}
		for {
			resp, err := c.ListPolicyFamilies(ctx, &pageReq, opts...)
			if err != nil {
				yield(nil, err)
				return
			}
			for i := range resp.PolicyFamilies {
				if !yield(&resp.PolicyFamilies[i], nil) {
					return
				}
			}
			if resp.NextPageToken == nil || *resp.NextPageToken == "" {
				return
			}
			pageReq.PageToken = resp.NextPageToken
		}
	}
}
