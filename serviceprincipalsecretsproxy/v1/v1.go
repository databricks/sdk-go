func (c *Client) ListServicePrincipalSecretsIter(ctx context.Context, req *ListServicePrincipalSecrets, opts ...api.Option) iter.Seq2[*ServicePrincipalSecret, error] {
	return func(yield func(*ServicePrincipalSecret, error) bool) {
		// Deep copy the request via JSON round-trip to avoid modifying the original.
		reqBody, err := json.Marshal(req)
		if err != nil {
			yield(nil, err)
			return
		}
		pageReq := ListServicePrincipalSecrets{}
		if err := json.Unmarshal(reqBody, &pageReq); err != nil {
			yield(nil, err)
			return
		}
		for {
			resp, err := c.ListServicePrincipalSecrets(ctx, &pageReq, opts...)
			if err != nil {
				yield(nil, err)
				return
			}
			for i := range resp.Secrets {
				if !yield(&resp.Secrets[i], nil) {
					return
				}
			}
			if resp.NextPageToken == nil || *resp.NextPageToken == "" {
				return
			}
			pageReq.PageToken = resp.NextPageToken
		}
	}
}
