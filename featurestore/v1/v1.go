func (c *Client) ListOnlineStoresIter(ctx context.Context, req *ListOnlineStoresRequest, opts ...api.Option) iter.Seq2[*OnlineStore, error] {
	return func(yield func(*OnlineStore, error) bool) {
		// Deep copy the request via JSON round-trip to avoid modifying the original.
		reqBody, err := json.Marshal(req)
		if err != nil {
			yield(nil, err)
			return
		}
		pageReq := ListOnlineStoresRequest{}
		if err := json.Unmarshal(reqBody, &pageReq); err != nil {
			yield(nil, err)
			return
		}
		for {
			resp, err := c.ListOnlineStores(ctx, &pageReq, opts...)
			if err != nil {
				yield(nil, err)
				return
			}
			for i := range resp.OnlineStores {
				if !yield(&resp.OnlineStores[i], nil) {
					return
				}
			}
			if resp.NextPageToken == nil || *resp.NextPageToken == "" {
				return
			}
			pageReq.PageToken = resp.NextPageToken
		}
	}
}

