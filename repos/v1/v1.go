func (c *Client) ListReposIter(ctx context.Context, req *ListRepos, opts ...api.Option) iter.Seq2[*RepoInfo, error] {
	return func(yield func(*RepoInfo, error) bool) {
		// Deep copy the request via JSON round-trip to avoid modifying the original.
		reqBody, err := json.Marshal(req)
		if err != nil {
			yield(nil, err)
			return
		}
		pageReq := ListRepos{}
		if err := json.Unmarshal(reqBody, &pageReq); err != nil {
			yield(nil, err)
			return
		}
		for {
			resp, err := c.ListRepos(ctx, &pageReq, opts...)
			if err != nil {
				yield(nil, err)
				return
			}
			for i := range resp.Repos {
				if !yield(&resp.Repos[i], nil) {
					return
				}
			}
			if resp.NextPageToken == nil || *resp.NextPageToken == "" {
				return
			}
			pageReq.NextPageToken = resp.NextPageToken
		}
	}
}

