func (c *Client) ListFeatureTagsIter(ctx context.Context, req *ListFeatureTagsRequest, opts ...api.Option) iter.Seq2[*FeatureTag, error] {
	return func(yield func(*FeatureTag, error) bool) {
		// Deep copy the request via JSON round-trip to avoid modifying the original.
		reqBody, err := json.Marshal(req)
		if err != nil {
			yield(nil, err)
			return
		}
		pageReq := ListFeatureTagsRequest{}
		if err := json.Unmarshal(reqBody, &pageReq); err != nil {
			yield(nil, err)
			return
		}
		for {
			resp, err := c.ListFeatureTags(ctx, &pageReq, opts...)
			if err != nil {
				yield(nil, err)
				return
			}
			for i := range resp.FeatureTags {
				if !yield(&resp.FeatureTags[i], nil) {
					return
				}
			}
			if resp.NextPageToken == nil || *resp.NextPageToken == "" {
				return
			}
			pageReq.PageToken = resp.NextPageToken
		}
	}
}

