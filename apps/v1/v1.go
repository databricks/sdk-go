func (c *Client) ListAppDeploymentsIter(ctx context.Context, req *ListAppDeploymentsRequest, opts ...api.Option) iter.Seq2[*AppDeployment, error] {
	return func(yield func(*AppDeployment, error) bool) {
		// Deep copy the request via JSON round-trip to avoid modifying the original.
		reqBody, err := json.Marshal(req)
		if err != nil {
			yield(nil, err)
			return
		}
		pageReq := ListAppDeploymentsRequest{}
		if err := json.Unmarshal(reqBody, &pageReq); err != nil {
			yield(nil, err)
			return
		}
		for {
			resp, err := c.ListAppDeployments(ctx, &pageReq, opts...)
			if err != nil {
				yield(nil, err)
				return
			}
			for i := range resp.AppDeployments {
				if !yield(&resp.AppDeployments[i], nil) {
					return
				}
			}
			if resp.NextPageToken == nil || *resp.NextPageToken == "" {
				return
			}
			pageReq.PageToken = resp.NextPageToken
		}
	}
}

