func (c *Client) ListNotificationDestinationsIter(ctx context.Context, req *ListNotificationDestinationsRequest, opts ...api.Option) iter.Seq2[*ListNotificationDestinationsResult, error] {
	return func(yield func(*ListNotificationDestinationsResult, error) bool) {
		// Deep copy the request via JSON round-trip to avoid modifying the original.
		reqBody, err := json.Marshal(req)
		if err != nil {
			yield(nil, err)
			return
		}
		pageReq := ListNotificationDestinationsRequest{}
		if err := json.Unmarshal(reqBody, &pageReq); err != nil {
			yield(nil, err)
			return
		}
		for {
			resp, err := c.ListNotificationDestinations(ctx, &pageReq, opts...)
			if err != nil {
				yield(nil, err)
				return
			}
			for i := range resp.Results {
				if !yield(&resp.Results[i], nil) {
					return
				}
			}
			if resp.NextPageToken == nil || *resp.NextPageToken == "" {
				return
			}
			pageReq.PageToken = resp.NextPageToken
		}
	}
}

